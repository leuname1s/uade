numeroMes = int(input("Ingrese el número del mes (1-12): "))
if numeroMes == 1:
    print("Enero")
elif numeroMes == 2:
    print("Febrero")
elif numeroMes == 3:
    print("Marzo")
elif numeroMes == 4:
    print("Abril")
elif numeroMes == 5:
    print("Mayo")
elif numeroMes == 6:
    print("Junio")
elif numeroMes == 7:
    print("Julio")
elif numeroMes == 8:
    print("Agosto")
elif numeroMes == 9:
    print("Septiembre")
elif numeroMes == 10:
    print("Octubre")
elif numeroMes == 11:
    print("Noviembre")
elif numeroMes == 12:
    print("Diciembre")
else:
    print("Número de mes inválido")